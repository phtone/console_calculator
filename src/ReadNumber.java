import java.util.Scanner;

public class ReadNumber {
    double one;
    double two;

    public ReadNumber(double numberOne, Double numberTwo) {
        one = numberOne;
        two = numberTwo;
    }

    double addition() {
        return one + two;

    }

    double subtract() {
        return one - two;
    }

    double multiply() {
        return one * two;

    }

}
