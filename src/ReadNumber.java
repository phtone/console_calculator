import java.util.Scanner;

public class ReadNumber {
    int one;
    int two;

    public ReadNumber(int numberOne, int numberTwo) {
        one = numberOne;
        two = numberTwo;
    }

    int addition() {

        return one + two;

    }

    int subtract() {
        return one - two;
    }

    int multiply() {

        return one * two;

    }

}
